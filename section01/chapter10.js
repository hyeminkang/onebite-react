// for
// for 를 이용하여 반복문을 반듬
for (let idx = 0; idx < 10; idx++) { 
  console.log("반복");
  if (idx % 2 === 0) {  
    console.log("짝수");
    continue;
  }

  console.log(idx);// 직관적으로 보고싶다면 

  if ( idx >= 5) { 
    break;
  }
}
